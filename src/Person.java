public class Person {
    private int age;
    private String placeOfWork;

    public Person(int age, String placeOfWork) {
        this.age = age;
        this.placeOfWork = placeOfWork;
    }

    public int getAge() {
        return age;
    }

    public String getPlaceOfWork() {
        return placeOfWork;
    }
}
